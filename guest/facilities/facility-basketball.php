			<a>basketball, game played between two teams of five players each on a rectangular court, usually indoors. 
                Each team tries to score by tossing the ball through the opponent’s goal, an elevated horizontal hoop 
                and net called a basket. The only major sport strictly of U.S. origin, basketball was invented by James 
                Naismith (1861–1939) on or about December 1, 1891, at the International Young Men’s Christian Association 
                (YMCA) Training School (now Springfield College), Springfield, Massachusetts, where Naismith was an instructor 
                in physical education. For that first game of basketball in 1891, Naismith used as goals two half-bushel peach 
                baskets, which gave the sport its name. The students were enthusiastic. After much running and shooting, William 
                R. Chase made a midcourt shot—the only score in that historic contest. Word spread about the newly invented game, 
                and numerous associations wrote Naismith for a copy of the rules, which were published in the January 15, 1892, 
                issue of the Triangle, the YMCA Training School’s campus paper. While basketball is competitively a winter sport, 
                it is played on a 12-month basis—on summer playgrounds, in municipal, industrial, and church halls, in school yards 
                and family driveways, and in summer camps—often on an informal basis between two or more contestants. Many grammar 
                schools, youth groups, municipal recreation centres, churches, and other organizations conduct basketball programs 
                for youngsters of less than high school age. Jay Archer, of Scranton, Pennsylvania, introduced “biddy” basketball in 
                1950 for boys and girls under 12 years of age, the court and equipment being adjusted for size.</a><br>
            <h1>History</h1>
            <a>In the early years the number of players on a team varied according to the number in the class and the size of the 
                playing area. In 1894 teams began to play with five on a side when the playing area was less than 1,800 square feet 
                (167.2 square metres); the number rose to seven when the gymnasium measured from 1,800 to 3,600 square feet (334.5 square metres) 
                and up to nine when the playing area exceeded that. In 1895 the number was occasionally set at five by mutual consent; the 
                rules stipulated five players two years later, and this number has remained ever since.</a><br>
            <a>Since Naismith and five of his original players were Canadians, it is not surprising that Canada was the first country outside 
                the United States to play the game. Basketball was introduced in France in 1893, in England in 1894, in Australia, China, and 
                India soon thereafter, and in Japan in 1900.</a><br>
            <a>While basketball helped swell the membership of YMCAs because of the availability of their gyms, within five years the game was 
                outlawed by various associations because gyms that had been occupied by classes of 50 or 60 members were now monopolized by only 
                10 to 18 players. The banishment of the game induced many members to terminate their YMCA membership and to hire halls to play the 
                game, thus paving the way to the professionalization of the sport.</a><br>
            <a>Originally, players wore one of three styles of uniforms: knee-length football trousers; jersey tights, as commonly worn by wrestlers; 
                or short padded pants, forerunners of today’s uniforms, plus knee guards. The courts often were of irregular shape with occasional 
                obstructions such as pillars, stairways, or offices that interfered with play. In 1903 it was ruled that all boundary lines must be 
                straight. In 1893 the Narragansett Machinery Co. of Providence, Rhode Island, marketed a hoop of iron with a hammock style of basket. 
                Originally a ladder, then a pole, and finally a chain fastened to the bottom of the net was used to retrieve a ball after a goal had 
                been scored. Nets open at the bottom were adopted in 1912–13. In 1895–96 the points for making a basket (goal, or field goal) were reduced 
                from three to two, and the points for making a free throw (shot uncontested from a line in front of the basket after a foul had been 
                committed) were reduced from three to one.</a><br>
            <a>Baskets were frequently attached to balconies, making it easy for spectators behind a basket to lean over the railings and deflect the ball 
                to favour one side and hinder the other; in 1895 teams were urged to provide a 4-by-6-foot (1.2-by-1.8-metre) screen for the purpose of 
                eliminating interference. Soon after, wooden backboards proved more suitable. Glass backboards were legalized by the professionals in 1908–09 
                and by colleges in 1909–10. In 1920–21 the backboards were moved 2 feet (0.6 metre), and in 1939–40 4 feet, in from the end lines to reduce frequent 
                stepping out-of-bounds. Fan-shaped backboards were made legal in 1940–41.</a><br>
            <a>A soccer ball (football) was used for the first two years. In 1894 the first basketball was marketed. It was laced, measured close to 32 inches (81 cm), 
                or about 4 inches (10 cm) larger than the soccer ball, in circumference, and weighed less than 20 ounces (567 grams). By 1948–49, when the laceless molded 
                ball was made official, the size had been set at 30 inches (76 cm).</a><br>
            <a>The first college to play the game was either Geneva College (Beaver Falls, Pennsylvania) or the University of Iowa. C.O. Bemis heard about the new sport 
                at Springfield and tried it out with his students at Geneva in 1892. At Iowa, H.F. Kallenberg, who had attended Springfield in 1890, wrote Naismith for 
                a copy of the rules and also presented the game to his students. At Springfield, Kallenberg met Amos Alonzo Stagg, who became athletic director at the 
                new University of Chicago in 1892. The first college basketball game with five on a side was played between the University of Chicago and the 
                University of Iowa in Iowa City on January 18, 1896. The University of Chicago won, 15–12, with neither team using a substitute. Kallenberg 
                refereed that game—a common practice in that era—and some of the spectators took exception to some of his decisions.</a><br>
            <h1>Court and Equipment</h1>
            <a>The standard American basketball court is in the shape of a rectangle 50 feet (15.2 metres) by 94 feet (28.7 metres); high school courts may be slightly smaller. 
                There are various markings on the court, including a centre circle, free throw lanes, and a three-point line, that help regulate play. A goal, or basket, 
                18 inches (46 cm) in diameter is suspended from a backboard at each end of the court. The metal rim of the basket is 10 feet (3 metres) above the floor. 
                In the professional game the backboard is a rectangle, 6 feet (1.8 metres) wide and 3.5 feet (1.1 metres) high, made of a transparent material, usually glass; 
                it may be 4 feet (1.2 metres) high in college. The international court varies somewhat in size and markings. The spherical inflated ball measures 29.5 to 30 
                inches (74.9 to 76 cm) in circumference and weighs 20 to 22 ounces (567 to 624 grams). Its covering is leather or composition.</a><br>
            <h1>Rules</h1>
            <a>The rules governing play of the game are based on Naismith’s five principles requiring a large, light ball, handled with the hands; 
                no running with the ball; no player being restricted from getting the ball when it is in play; no personal contact; and a horizontal, 
                elevated goal. The rules are spelled out in specific detail by the governing bodies of the several branches of the sport and cover 
                the playing court and equipment, officials, players, scoring and timing, fouls, violations, and other matters. The officials include 
                a referee and two umpires in college play (two referees and a crew chief in NBA play), two timers, and two scorekeepers. One player on 
                each team acts as captain and speaks for the team on all matters involving the officials, such as interpretation of rules. Professional, 
                international, and high school games are divided into four periods, college games into two.</a><br>
            <a>Since the 1895–96 season, a field goal has scored two points and a free throw one point. When the ABA was founded in 1967, it allowed 
                three points for shots made from outside a boundary line set 25 feet (7.6 metres) from the basket. With varying distances, the change 
                was adopted officially by the NBA in 1979 and, in 1985, by colleges.</a><br>
            <a>Basketball is a rough sport, although it is officially a noncontact game. A player may pass or bounce (dribble) the ball to a position whereby he or a teammate may try for a basket. A foul is committed whenever a player makes such contact with an opponent as to put him at a disadvantage; for the 2001–02 season the NBA approved a rule change that eliminated touch fouls, meaning brief contact initiated by a defensive player is allowable if it does not impede the progress of the offensive player. If a player is fouled while shooting and the shot is good, the basket counts and he is awarded one free throw (an unhindered throw for a goal from behind the free throw, or foul, line, which is 15 feet [4.6 metres] from the backboard); if the shot misses, he gets a second free throw. If a foul is committed against a player who is not shooting, then his team is awarded either the possession of the ball or a free throw if the other team is in a penalty situation. A team is in a penalty situation when it has been called for a set number of fouls in one period (five in one quarter in professional and international play and seven in one half in the college game). In college basketball, penalty free throws are “one-and-one” in nature (consisting of one free throw that, if made, is followed by a second) until the opposing team commits a 10th foul in a half, creating a “double bonus” situation where all fouls automatically result in two free throws. A pair of penalty free throws are immediately earned when teams enter the penalty situation in both the NBA and international play. Infractions such as unsportsmanlike conduct or grasping the rim are technical fouls, which award to the opposition a free throw and possession of the ball. Overly violent fouls are called flagrant fouls and also result in free throws and possession for the opposition. Players are allowed a set number of personal fouls per game (six in the NBA, five in most other competitions) and are removed from the game when the foul limit is reached.</a><br>
            <a>Other common infractions occur when a player (with the ball) takes an excessive number of steps or slides; fails to advance the ball within five seconds while being “closely guarded”; causes the ball to go out-of-bounds; steps over the foul line while shooting a free throw; steps over the end line or sideline while tossing the ball in to a teammate, or fails to pass the ball in within five seconds; runs with, kicks, or strikes the ball with his fist; dribbles a second time after having once concluded his dribble (double dribble); remains more than three seconds in his free throw lane while he or his team has the ball; causes the ball to go into the backcourt; retains the ball in the backcourt more than 10 seconds, changed in the NBA to 8 seconds for 2001–02; or fails to shoot within the time allotted by the shot clock (24 seconds in the NBA, the WNBA, and international play; 30 in women’s college basketball; and 35 in men’s college basketball). The penalty is loss of the ball—opponents throw the ball in from the side.</a>

