            <h1>Court and Equipment</h1>
            <a>The object of football is to maneuver the ball into the opposing team’s goal, using any part of the body except the hands and arms. The side scoring more goals wins. The ball is round, covered with leather or some other suitable material, and inflated; it must be 27–27.5 inches (68–70 cm) in circumference and 14.5–16 ounces (410–450 grams) in weight. A game lasts 90 minutes and is divided into halves; the halftime interval lasts 15 minutes, during which the teams change ends. Additional time may be added by the referee to compensate for stoppages in play (for example, player injuries). If neither side wins, and if a victor must be established, “extra-time” is played, and then, if required, a series of penalty kicks may be taken.</a><br>
