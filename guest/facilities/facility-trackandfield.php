			<a>Volleyball, game played by two teams, usually of six players on a side, 
                in which the players use their hands to bat a ball back and forth over a high net, trying 
                to make the ball touch the court within the opponents’ playing area before it can be returned. 
                To prevent this a player on the opposing team bats the ball up and toward a teammate before it 
                touches the court surface—that teammate may then volley it back across the net or bat it to a third 
                teammate who volleys it across the net. A team is allowed only three touches of the ball before it 
                must be returned over the net.</a><br>
            <h1>History</h1>
            <a>Volleyball was invented in 1895 by William G. Morgan, physical director of the Young Men’s Christian 
                Association (YMCA) in Holyoke, Massachusetts. It was designed as an indoor sport for businessmen who 
                found the new game of basketball too vigorous. Morgan called the sport “mintonette,” until a professor 
                from Springfield College in Massachusetts noted the volleying nature of play and proposed the name of 
                “volleyball.” The original rules were written by Morgan and printed in the first edition of the Official 
                Handbook of the Athletic League of the Young Men’s Christian Associations of North America (1897). The game 
                soon proved to have wide appeal for both sexes in schools, playgrounds, the armed forces, and other organizations 
                in the United States, and it was subsequently introduced to other countries.</a><br>
            <a>In 1916 rules were issued jointly by the YMCA and the National Collegiate Athletic Association (NCAA). The first 
                nationwide tournament in the United States was conducted by the National YMCA Physical Education Committee in New 
                York City in 1922. The United States Volleyball Association (USVBA) was formed in 1928 and recognized as the rules-making, 
                governing body in the United States. From 1928 the USVBA—now known as USA Volleyball (USAV)—has conducted annual national 
                men’s and senior men’s (age 35 and older) volleyball championships, except during 1944 and 1945. Its women’s division was 
                started in 1949, and a senior women’s division (age 30 and older) was added in 1977. Other national events in the United 
                States are conducted by member groups of the USAV such as the YMCA and the NCAA.</a><br>
            <a>Volleyball was introduced into Europe by American troops during World War I, when national organizations were formed. The 
                Fédération Internationale de Volley Ball (FIVB) was organized in Paris in 1947 and moved to Lausanne, Switzerland, in 1984. 
                The USVBA was one of the 13 charter members of the FIVB, whose membership grew to more than 210 member countries by the late 
                20th century.</a><br>
            <a>International volleyball competition began in 1913 with the first Far East Games, in Manila. During the early 1900s and continuing 
                until after World War II, volleyball in Asia was played on a larger court, with a lower net, and nine players on a team. The FIVB-sponsored 
                world volleyball championships (for men only in 1949; for both men and women in 1952 and succeeding years) led to acceptance of standardized 
                playing rules and officiating. Volleyball became an Olympic sport for both men and women at the 1964 Olympic Games in Tokyo.</a><br>
            <a>European championships were long dominated by Czechoslovakian, Hungarian, Polish, Bulgarian, Romanian, and Soviet (later, Russian) teams. 
                At the world and Olympic level, Soviet teams have won more titles, both men’s and women’s, than those of any other nation. Their success 
                was attributed to widespread grassroots interest and well-organized play and instruction at all levels of skill. A highly publicized Japanese 
                women’s team, Olympic champions in 1964, reflected the interest of private industry in sport. Young women working for the sponsoring company 
                devoted their free time to conditioning, team practice, and competition under expert and demanding coaching. Encouraged by the Japanese Volleyball 
                Association, this women’s team made its mark in international competition, winning the World Championship in 1962, 1966, and 1967, in addition to 
                the 1964 Olympics. At the end of the 20th century, however, the Cuban women’s team dominated both the World Championships and the Olympics.</a><br>
            <a>The Pan American Games (involving South, Central, and North America) added volleyball in 1955, and Brazil, Mexico, Canada, Cuba, and the United 
                States are frequent contenders for top honours. In Asia, China, Japan, and Korea dominate competition. Volleyball, especially beach volleyball, 
                is played in Australia, New Zealand, and throughout the South Pacific. A four-year cycle of international volleyball events, recommended by the 
                FIVB, began in 1969 with World Cup championships, to be held in the year following the Olympic Games; the second year is the World Championships; 
                in the third the regional events are held (e.g., European championships, Asian Games, African Games, Pan American Games); and in the fourth year 
                the Olympic Games.</a><br>
            <h1>Court and Equipment</h1>
            <a>Volleyball requires a minimum of equipment and space and can be played indoors or outdoors. The game is played on a smooth-surfaced court 9 metres 
                (30 feet) wide by 18 metres (60 feet) long, divided by a centre line into two equal areas, one of which is selected by or assigned to each of the 
                two competing teams. Players may not step completely beyond the centre line while the ball is in play. A line 3 metres (10 feet) from and parallel 
                to the centre line of each half of the court indicates the point in front of which a back court player may not drive the ball over the net from a 
                position above the top of the net. (This offensive action, called a spike, or kill, is usually performed most effectively and with greatest power 
                near the net by the forward line of players.) A tightly stretched net is placed across the court exactly above the middle of the centre line; official 
                net heights (measured from the top edge of the net to the playing surface—in the middle of the court) are 2.4 metres (8 feet) for men and 2.2 metres 
                (7.4 feet) for women. Further adjustments in net height can be made for young people and others who need a lower net. A vertical tape marker is attached 
                to the net directly above each side boundary line of the court, and, to help game officials judge whether served or volleyed balls are in or out of bounds, 
                a flexible antenna extends 1 metre (3 feet) above the net along the outer edge of each vertical tape marker. The ball used is around 260 to 280 grams (9 to 10 ounces) 
                and is inflated to about 65 cm (25.6 inches) in circumference. A ball must pass over the net entirely between the antennae. A service area, traditionally 3 metres 
                (10 feet) long, is marked outside and behind the right one-third of each court end line. At the 1996 Olympic Games the service area was extended to 9 metres (30 feet). 
                The service must be made from within or behind this area. A space at least 2 metres (6 feet) wide around the entire court is needed to permit freedom of action, 
                eliminate hazards from obstructions, and allow space for net support posts and the officials’ stands. A clear area above the court at least 8 metres (26 feet) 
                high is required to permit the ball to be served or received and played without interference.</a><br> 
                
            <h1>Rules</h1>
                <a>Informally, any number can play volleyball. In competition each team consists of six players, three of whom take the forward positions in a row close to 
                and facing the net, the other three playing the back court. (An exception to this rotation is the libero, a position introduced at the 2000 Olympics; see below.) 
                Play is started when the right back (the person on the right of the second row) of the serving team steps outside his end line into the serving area and bats the ball with a hand, fist, or arm over the net into 
                the opponents’ half of the court. The opponents receive the ball and return it across the net in a series of not more than three contacts with the ball. 
                This must be done without any player catching or holding the ball while it is in play and without any player touching the net or entering the opponents’ 
                court area. The ball must not touch the floor, and a player may not touch the ball twice in succession. A player continues to serve until his team makes 
                an error, commits a foul, or completes the game. When the service changes, the receiving team becomes the serving team and its players rotate clockwise 
                one position, the right forward shifting to the right back position and then serving from the service area. Either team can score, with points being awarded 
                for successfully hitting the ball onto the opposing side’s half of the court, as well as when the opposing side commits errors or fouls, such as hitting 
                the ball out of bounds, failing to return the ball, contacting the ball more than three times before returning it, etc. Only one point at a time is scored 
                for a successful play. A game is won by the team that first scores 25 points, provided the winning team is ahead by 2 or more points, except in the fifth set, 
                when a team needs to score only 15 points and win by 2 points.</a><br> 
                <a>The 2000 Olympics introduced significant rule changes to international competition. One change 
                created the libero, a player on each team who serves as a defensive specialist. The libero wears a different colour from the rest of the team and is not allowed 
                to serve or rotate to the front line. Another important rule change allowed the defensive side to score, whereas formerly only the serving team was awarded points.</a>

